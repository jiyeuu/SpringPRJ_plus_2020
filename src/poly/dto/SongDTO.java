package poly.dto;

public class SongDTO {
	
	private String song; //노래제목
	private String singer; //가수
	
	private String hap;
	private String nes;
	private String pos;
	
	private int hap2;
	private int nes2;
	private int pos2;
	public String getSong() {
		return song;
	}
	public void setSong(String song) {
		this.song = song;
	}
	public String getSinger() {
		return singer;
	}
	public void setSinger(String singer) {
		this.singer = singer;
	}
	public String getHap() {
		return hap;
	}
	public void setHap(String hap) {
		this.hap = hap;
	}
	public String getNes() {
		return nes;
	}
	public void setNes(String nes) {
		this.nes = nes;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public int getHap2() {
		return hap2;
	}
	public void setHap2(int hap2) {
		this.hap2 = hap2;
	}
	public int getNes2() {
		return nes2;
	}
	public void setNes2(int nes2) {
		this.nes2 = nes2;
	}
	public int getPos2() {
		return pos2;
	}
	public void setPos2(int pos2) {
		this.pos2 = pos2;
	}
	
	
	
	

}
